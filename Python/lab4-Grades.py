grade = int(input('Please enter the numeric number of your grade: '))

if grade >= 90 :
    print('Congratulations! You got an A!')
elif grade >=80 :
    print('You got a B, Nice work!')
elif grade >=70 :
    print('Your grade is a \'C\' and there is room for improvement ')
elif grade >=60 :
    print('You have a D. Time to put in work.')
else :
    print('You got a failing grade, see me after class.')